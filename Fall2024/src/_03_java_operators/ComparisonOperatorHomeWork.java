package _03_java_operators;

public class ComparisonOperatorHomeWork {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 8;
        System.out.println(num1 == num2);
        System.out.println(num2 == num1);
        System.out.println(num1 < num2);
        System.out.println(num2 < num1);
        System.out.println(num1 <= num2);
        System.out.println(num1 >= num2);

    }
}
